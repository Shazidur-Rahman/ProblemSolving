/*
Given two numbers A and B. Your task is to return the sum of A and B.
Example 1:
Input:
A = 1, B = 2
Output:
3
Explanation:
Addition of 1 and 2 is 3.
 */
public class Set_14 {
	static int addition(int a, int b){
		return a + b ;
	}

	public static void main(String[] args) {
		System.out.println(addition(5, 10));
	}
}
